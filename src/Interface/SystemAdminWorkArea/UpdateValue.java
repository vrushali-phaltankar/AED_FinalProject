    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String newValue = jTextField1.getText();
        
        if(null == newValue || newValue.isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please enter value");
            jTextField1.setText("");
			return;
        }
        
        if(object instanceof City)
        {
            Pattern p = Pattern.compile("^[a-zA-Z\\s]+");
            Matcher m = p.matcher(newValue);  
            if(!m.matches())
            {
                JOptionPane.showMessageDialog(null, "Invalid City Name");
			return;
            }
            
            City existingCity = (City)object;
            
            if (null != system.getCitiesList() && !system.getCitiesList().isEmpty())
		{
                        for (City city : system.getCitiesList())
			{
				if (newValue.equalsIgnoreCase(city.getName()))
				{
                                    JOptionPane.showMessageDialog(null, "City Already Exist in System");
                                    return;
				}
			}
                    
			for (City city : system.getCitiesList())
			{
				if (existingCity.getName().equalsIgnoreCase(city.getName()))
				{
					city.setName(newValue);
                                        break;
				}
			}
		}
            JOptionPane.showMessageDialog(null, "City Updated Successfully");
		
            if(classType instanceof ManageCity)
            {
                ((ManageCity)classType).populateCityTable();
            }
        }
        this.setVisible(false);
        
    }//GEN-LAST:event_jButton1ActionPerformed

