    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed

        String userName = txtUserName.getText();
        char[] passwordCharArray = txtPassword.getPassword();
        String password = String.valueOf(passwordCharArray);

        UserAccount userAccount = system.getUserAccountDirectory().authenticateUser(userName, password);

        Enterprise inEnterprise = null;
        Organization inOrganization = null;

         if (userAccount == null) {
            //Step2: Go inside each city to check each enterprise
            for (City city : system.getCitiesList()) {
                //Step 2-a: Check against each enterprise
                for (Enterprise enterprise : city.getEnterpriseDirectory().getEnterpriseArrayList()) {
                    userAccount = enterprise.getUserAccountDirectory().authenticateUser(userName, password);
                    if (userAccount == null) {
                        //Step3: Check against each organization inside that enterprise
                        if(enterprise instanceof FoodManagementEnterprise){
                            for (Community com: ((FoodManagementEnterprise)enterprise).getCommunityArrayList()){
                               
                                //community admin                                
                               userAccount =  com.getCommunityOrganization().getUserAccountDirectory().authenticateUser(userName, password);
                               
                               if (userAccount == null){
                                   // food collection
                                   userAccount = com.getFoodCollectionOrganization().getUserAccountDirectory().authenticateUser(userName, password);
                                   
                                   if (userAccount == null){
                                       // food standard 
                                       userAccount = com.getFoodStandardOrganization().getUserAccountDirectory().authenticateUser(userName, password);
                                       
                                       if(userAccount == null){
                                           //food distribution
                                           userAccount = com.getFoodDistributionOrganization().getUserAccountDirectory().authenticateUser(userName, password);
                                           
                                                if (userAccount == null){
                                                    //resident
                                                    userAccount = com.getResidentOrganization().getUserAccountDirectory().authenticateUser(userName, password);
                                                    if(userAccount == null){
                                                        //commerical
                                                        userAccount = com.getRestaurantOrganization().getUserAccountDirectory().authenticateUser(userName, password);
                                                          if (userAccount == null){   
                                                                inEnterprise = enterprise;
                                                                inOrganization =null;
                                                                //break;
                                                          }
                                                          else{
                                                                inEnterprise = enterprise;
                                                                inOrganization = com.getRestaurantOrganization();
                                                                break;
                                                          }
                                                        
                                                    }
                                                    else{
                                                       inEnterprise = enterprise;
                                                       inOrganization = com.getResidentOrganization();
                                                       break;
                                                    }
                                                
                                                
                                                }
                                                else{
                                                    inEnterprise = enterprise;
                                                    inOrganization = com.getFoodDistributionOrganization();
                                                    break;
                                                }
                                                 
                                       }
                                       else {
                                           
                                            inEnterprise = enterprise;
                                            inOrganization = com.getFoodStandardOrganization();
                                            break;
                                           
                                       }
                                       
                                       
                                   }
                                   else {
                                       inEnterprise = enterprise;
                                       inOrganization = com.getFoodCollectionOrganization();
                                       break;
                                       
                                   }
                                   
                               }
                               else {
                                   inEnterprise = enterprise;
                                   inOrganization = com.getCommunityOrganization();
                                break;
                               }
                                
                            }
                            
                        }
                        else {
                         for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationArrayList()) {
                            userAccount = organization.getUserAccountDirectory().authenticateUser(userName, password);
                            if (userAccount != null) {
                                inEnterprise = enterprise;
                                inOrganization = organization;
                                break;
                            }
                        }
                        }
                        
                       
                    } else {
                        inEnterprise = enterprise;
                        break;
                    }
                    if (inOrganization != null) {
                        break;
                    }
                }
                if (inEnterprise != null) {
                    break;
                }
            }
        }

        if (userAccount == null) {
            JOptionPane.showMessageDialog(null, "Invalid Credentails!");
            return;
        } else {
            
            btnLogout.setEnabled(true);
            txtUserName.setText("");
            txtPassword.setText("");
            btnLogin.setEnabled(false);
            
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            userProcessContainer.add("workArea", userAccount.getRole().createWorkArea(userProcessContainer, userAccount, inOrganization, inEnterprise, system));
            layout.next(userProcessContainer);
                        
        }

    }//GEN-LAST:event_btnLoginActionPerformed

