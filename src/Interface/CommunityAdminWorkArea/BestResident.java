    public ArrayList<ResidentEmployee> calculateTopThreeResidentByRewardPoints(ArrayList<ResidentEmployee> residentList){
        
        int val = 0;
         
       ResidentEmployee ce1 =   new ResidentEmployee();
       ResidentEmployee ce2 =   new ResidentEmployee();
       ResidentEmployee ce3 =   new ResidentEmployee();
        
        for(ResidentEmployee ce : residentList){
            
            val =  ce.getCurrentRewardPoints();
             if ( val > ce1.getCurrentRewardPoints()){
                ce3=ce2;
                ce2=ce1;
                ce1=ce;
            }
            else if (val>ce2.getCurrentRewardPoints()){
                if (ce1.getCurrentRewardPoints()==val){
                    if (ce1.getCurrentRewardPoints() != ce.getCurrentRewardPoints()){
                        ce3=ce2;
                        ce2=ce; 
                    }
                }
                else
                {
                    ce3=ce2;
                    ce2=ce;
                }
            }
             
            else if(val>ce3.getCurrentRewardPoints()){
                if(ce1.getCurrentRewardPoints()==val || ce2.getCurrentRewardPoints()==val){
                    
                }
                else{
                    ce3=ce;
                }
            } 
            
            
        }
        
        ArrayList<ResidentEmployee> ceList= new ArrayList<ResidentEmployee>();
        ceList.add(ce1);
        ceList.add(ce2);
        ceList.add(ce3);
        
        return ceList;
        
        
        
    }

