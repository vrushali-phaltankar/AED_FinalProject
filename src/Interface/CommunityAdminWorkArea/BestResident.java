    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        // TODO add your handling code here:
        Date toDate1=jDateChooser1.getDate();
        Date toDate2=jDateChooser2.getDate();
        if ((toDate1==null) || (toDate2==null)){
            JOptionPane.showMessageDialog(null, "Invalid date..Kindly enter valid date.");
            return;
            
        }
        
        long fromDate = (jDateChooser1.getDate().getTime())/(1000*60*60*24);
        long toDate =  (jDateChooser2.getDate().getTime())/(1000*60*60*24);
        //String residentRewards = null;
        
        
        
        ArrayList<ResidentEmployee> residentEmployeeList = new ArrayList<>();

        for(WorkRequest request : organization.getWorkQueue().getWorkRequestArrayList()){

            long requestDate =  (request.getRequestDate().getTime())/(1000*60*60*24);

            if ((requestDate>=fromDate) && (requestDate <= toDate) ){
                
                Employee ee = request.getSender().getEmployee();
                if(ee instanceof ResidentEmployee){
                     ResidentEmployee c = (ResidentEmployee) ee;
                     if(!residentEmployeeList.contains(c)){                    
                        c.calculateTotalRewardPointsForResident();                     
                        residentEmployeeList.add(c);      
                     }
                }
            }
//            else{
//
//                JOptionPane.showMessageDialog(null, "There are no records for this search criteria.");
//            }

        }
        
        if(residentEmployeeList.isEmpty()){
            JOptionPane.showMessageDialog(null, "There are no records for this search criteria.");
        }
                
                
        ArrayList<ResidentEmployee> residentEmployee = calculateTopThreeResidentByRewardPoints(residentEmployeeList);
        

        DefaultCategoryDataset dataset = new DefaultCategoryDataset();
        dataset.setValue(residentEmployee.get(0).getCurrentRewardPoints(), "Residents", residentEmployee.get(0).getName());
        dataset.setValue(residentEmployee.get(1).getCurrentRewardPoints(), "Residents", residentEmployee.get(1).getName());
        dataset.setValue(residentEmployee.get(2).getCurrentRewardPoints(),  "Residents",residentEmployee.get(2).getName());

        JFreeChart chart = ChartFactory.createBarChart3D("Types of food collected", "Top 3 resident", "Number of reward points", dataset, PlotOrientation.VERTICAL, false, true, false);
        CategoryPlot plot = chart.getCategoryPlot();
        plot.setRangeGridlinePaint(Color.BLUE);
        ChartFrame frame = new ChartFrame("Bar Chart for Types of food collected",chart);
        frame.setVisible(true);
        frame.setSize(450, 350);

    }//GEN-LAST:event_btnCalculateActionPerformed

